const mongoose = require('mongoose');

const Schema = mongoose.Schema;


const userSchema = new Schema({
    username: {
        type: String,
        required: [true, "Please provide a username"],
        unique: true,
    },
    email: {
        type: String,
        required: [true, "Please provide a email"],
        unique: true,
    },
    password: {
        type: String,
        required: [true, "Please provide a password"],
    },
    isVerfied: {
        type: Boolean,
        default: false,
    },
    isAdmin: {
        type: Boolean,
        default: false,
    },
    pdf:{
        type:String

    }
    
})

export const User = mongoose.models.userModel ||  mongoose.model("userModel", userSchema);
